    private void BotBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotBuscarActionPerformed
        // TODO add your handling code here:
         String numeroConta = CampoConta.getText().trim();

    if (numeroConta.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Digite o nÃºmero da conta.");
        return;
    }

    try {
        var conta = GuiController.getBancoService().buscarConta(numeroConta);

        // TITULAR
        LabelTitular.setText("Titular: " + conta.getProprietario().getNome());
        LabelCPF.setText("CPF: " + conta.getProprietario().getCpf());
        LabelTipo.setText("Tipo: " + conta.getTipo());
        LabelSaldo.setText("Saldo:R$ " + String.format("%.2f", conta.getSaldo()));

        // =============================
        // MOSTRAR OU ESCONDER LIMITE
        // =============================
        if (conta instanceof entidades.ContaCorrente cc) {

            double limite = cc.getLimiteChequeEspecial();

            LabelCheque.setText("Limite Cheque Especial: R$ " + 
                String.format("%.2f", limite));

            LabelCheque.setVisible(true);  // ðŸ‘ˆ mostra o label

        } else {
            LabelCheque.setVisible(false); // ðŸ‘ˆ esconde o label
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Conta nÃ£o encontrada!");
    }
    }//GEN-LAST:event_BotBuscarActionPerformed

