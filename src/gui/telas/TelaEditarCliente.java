    private void BotBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotBuscarActionPerformed
        // TODO add your handling code here:
        String cpf = CampoCPF.getText().trim();
        
        if(cpf.isEmpty()){
         JOptionPane.showMessageDialog(this, "Digite um CPF ");
         return;
        }try {
            var cliente = GuiController.getBancoService().buscarCliente(cpf);
            CampoNome.setText(cliente.getNome());
            CampoEndereco.setText(cliente.getEndereco());
        }catch(Exception e){
         JOptionPane.showMessageDialog(this, "Cliente sem cadastro ou nao encontrado");
        }
                    
        
    }//GEN-LAST:event_BotBuscarActionPerformed

