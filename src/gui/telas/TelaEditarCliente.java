    private void BotSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotSalvarActionPerformed
        // TODO add your handling code here:
        String cpf = CampoCPF.getText().trim();
        
        if(cpf.isEmpty()){
         JOptionPane.showMessageDialog(this, "Campo CPF tem que estar preenchido");
         return;
        }
        try{
         var cliente = GuiController.getBancoService().buscarCliente(cpf);
         String attNome = CampoNome.getText().trim();
         if(!attNome.isEmpty()){
          cliente.setNome(attNome);
         }
         String attEndereco = CampoEndereco.getText().trim();
         if(!attEndereco.isEmpty()){
             cliente.setEndereco(attEndereco);
        }
         JOptionPane.showMessageDialog(this, "Dados atualizados");
        } catch(Exception e){
            JOptionPane.showMessageDialog(this, "Cliente nao encontrado");
        }
    }//GEN-LAST:event_BotSalvarActionPerformed

