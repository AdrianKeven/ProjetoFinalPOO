    private void BotDesativarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotDesativarActionPerformed
         String numeroContaTexto = CampoConta.getText().trim();

    if (numeroContaTexto.isEmpty()) {
        JOptionPane.showMessageDialog(this, "O campo N√∫mero da Conta n√£o pode estar vazio.");
        return;
    }

    try {
        // Buscar conta apenas para mostrar os dados e checar saldo
        Conta conta = GuiController.getBancoService().buscarConta(numeroContaTexto);

        // Confirma√ß√£o antes de desativar
        int resposta = JOptionPane.showConfirmDialog(
            this,
            "Tem certeza que deseja desativar esta conta?",
            "Confirma√ß√£o",
            JOptionPane.YES_NO_OPTION
        );

        if (resposta != JOptionPane.YES_OPTION) {
            return; // Usu√°rio cancelou
        }

        // Verifica saldo antes de remover
        if (conta.getSaldo() != 0) {
            JOptionPane.showMessageDialog(this,
                "üî¥ A conta possui saldo. √â preciso zerar antes de desativar.",
                "Saldo n√£o zerado",
                JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Remover conta usando o n√∫mero (String)
        GuiController.getBancoService().removerConta(numeroContaTexto);

        JOptionPane.showMessageDialog(this, "Conta desativada com sucesso!");

        // Limpar campos e labels
        CampoConta.setText("");
        jLabel1.setText("Nome:");
        jLabel2.setText("CPF:");
        jLabel3.setText("Saldo:");
        jLabel6.setText("Tipo de conta:");
        BotDesativar.setEnabled(false);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro ao desativar a conta: " + e.getMessage());
    }
    }//GEN-LAST:event_BotDesativarActionPerformed

