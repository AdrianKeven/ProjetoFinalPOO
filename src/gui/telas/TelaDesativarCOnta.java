    private void BotBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotBuscarActionPerformed
        // TODO add your handling code here:
        String numeroTexto = CampoConta.getText().trim();   

    if(numeroTexto.isEmpty()){
        JOptionPane.showMessageDialog(this, "Campo n√∫mero da conta n√£o pode estar vazio");
        return;
    }

    try{
        String numeroConta = CampoConta.getText().trim();
        var conta = GuiController.getBancoService().buscarConta(numeroConta);

        // Preencher labels
        jLabel1.setText("Nome: " + conta.getProprietario().getNome());
        jLabel2.setText("CPF: " + conta.getProprietario().getCpf());
        jLabel3.setText("Saldo: R$ " + String.format("%.2f", conta.getSaldo()));
        jLabel6.setText("Tipo de conta: " + conta.getNumero());

        // Habilitar ou n√£o o bot√£o de desativar
        if(conta.getSaldo() != 0){
            JOptionPane.showMessageDialog(this,
                "üî¥ A conta possui saldo. √â preciso zerar antes de desativar.",
                "Saldo n√£o zerado",
                JOptionPane.WARNING_MESSAGE);
            BotDesativar.setEnabled(false);
        } else {
            BotDesativar.setEnabled(true);
        }

    } catch(NumberFormatException e){
        JOptionPane.showMessageDialog(this, "N√∫mero da conta inv√°lido");
    } catch(Exception e){
        JOptionPane.showMessageDialog(this, "Conta n√£o encontrada");
        BotDesativar.setEnabled(false);
    }
    }//GEN-LAST:event_BotBuscarActionPerformed

