    private void BotExecutarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotExecutarActionPerformed
        // TODO add your handling code here:
    if(!origemConfirmada || !destinoConfirmada){
        JOptionPane.showMessageDialog(this, "Confirme as contas antes de transferir.");
        return;
    }

    String txt = CampoValor.getText().trim();
    if(txt.isEmpty()){
        JOptionPane.showMessageDialog(this, "Digite o valor da transferência.");
        return;
    }

    try {
        double valor = Double.parseDouble(txt.replace(",", "."));

        if(valor <= 0){
            JOptionPane.showMessageDialog(this, "Informe um valor maior que zero.");
            return;
        }

        double saldo = contaOrigem.getSaldo();
        double limite = 0;
        boolean usarChequeEspecial = false;

        if(contaOrigem instanceof ContaCorrente cc){
            limite = cc.getLimiteChequeEspecial();
        }

        // Verificar se usará cheque especial
        if(valor > saldo){
            usarChequeEspecial = true;
        }

        // Verificação de limite total (saldo + cheque especial)
        if(saldo + limite < valor){
            JOptionPane.showMessageDialog(this, "Saldo insuficiente para transferência!");
            return;
        }

        // ==============================
        // CONFIRMAÇÃO DE USO DO CHEQUE
        // ==============================
        if(usarChequeEspecial){
            double valorUsadoDoCheque = valor - saldo;

            int respCheque = JOptionPane.showConfirmDialog(
                this,
                String.format(
                    "Você está prestes a usar o cheque especial.\n\n" +
                    "Valor da transferência: R$ %.2f\n" +
                    "Saldo disponível:       R$ %.2f\n" +
                    "Valor no cheque:        R$ %.2f\n\n" +
                    "Deseja continuar?",
                    valor, saldo, valorUsadoDoCheque
                ),
                "Confirmar uso do Cheque Especial",
                JOptionPane.YES_NO_OPTION
            );

            if(respCheque != JOptionPane.YES_OPTION){
                return;
            }
        }

        // ==============================
        // CONFIRMAÇÃO DE RESUMO FINAL
        // ==============================
        int respFinal = JOptionPane.showConfirmDialog(
            this,
            "CONFIRMAR TRANSFERÊNCIA?\n\n" +
            "Origem:  " + contaOrigem.getNumero() + " - " + contaOrigem.getProprietario().getNome() + "\n" +
            "Destino: " + contaDestino.getNumero() + " - " + contaDestino.getProprietario().getNome() + "\n" +
            "Valor:   R$ " + String.format("%.2f", valor) + "\n\n" +
            "Deseja executar a operação?",
            "Confirmação",
            JOptionPane.YES_NO_OPTION
        );

        if(respFinal != JOptionPane.YES_OPTION){
            return;
        }

        // ==============================
        // REALIZAR TRANSFERÊNCIA
        // ==============================
        GuiController.getBancoService().realizarTransferencia(
                contaOrigem.getNumero(),
                contaDestino.getNumero(),
                valor
        );

        // ==============================
        // ATUALIZAR TELA (recarregar do banco)
        // ==============================
        contaOrigem = GuiController.getBancoService().buscarConta(contaOrigem.getNumero());
        contaDestino = GuiController.getBancoService().buscarConta(contaDestino.getNumero());

        LabelSaldo.setText("Saldo: R$ " + String.format("%.2f", contaOrigem.getSaldo()));

        if(contaOrigem instanceof ContaCorrente ccOrigem){
            LabelCheque.setText("Cheque Especial: R$ " + String.format("%.2f", ccOrigem.getLimiteChequeEspecial()));
        }

        CampoValor.setText("");

        JOptionPane.showMessageDialog(this, "Transferência realizada com sucesso!");

    } catch (NumberFormatException e){
        JOptionPane.showMessageDialog(this, "Valor inválido!");
    } catch (Exception e){
        JOptionPane.showMessageDialog(this, "Erro ao transferir: " + e.getMessage());
    }
    }//GEN-LAST:event_BotExecutarActionPerformed

