    private void BotBuscarOrigemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotBuscarOrigemActionPerformed
        // TODO add your handling code here:
        String numero = CampoOrigem.getText().trim();

    if(numero.isEmpty()){
        JOptionPane.showMessageDialog(this, "Digite o número da conta de origem.");
        return;
    }

    try {
        contaOrigem = GuiController.getBancoService().buscarConta(numero);

        LabelNomeOrigem.setText("Nome: " + contaOrigem.getProprietario().getNome());
        LabelCPFOrigem.setText("CPF: " + contaOrigem.getProprietario().getCpf());
        LabelTipoOrigem.setText("Tipo: " + contaOrigem.getTipo());
        LabelSaldo.setText("Saldo: R$ " + String.format("%.2f", contaOrigem.getSaldo()));

        if(contaOrigem instanceof ContaCorrente cc){
            LabelCheque.setText("Cheque Especial: R$ " + String.format("%.2f", cc.getLimiteChequeEspecial()));
            LabelCheque.setVisible(true);
        } else {
            LabelCheque.setVisible(false);
        }

    } catch (Exception e){
        JOptionPane.showMessageDialog(this, "Conta não encontrada!");
    }
    }//GEN-LAST:event_BotBuscarOrigemActionPerformed

